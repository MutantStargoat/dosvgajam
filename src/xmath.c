#define XMUL(a, b)	((int32_t)((int64_t)(a) * (int64_t)(b) >> 16))

void vec3_xform(int32_t *v, const int32_t *m)
{
	int32_t x, y;
	x = XMUL(m[0], v[0]) + XMUL(m[1], v[1]) + XMUL(m[2], v[2]) + m[3];
	y = XMUL(m[4], v[0]) + XMUL(m[5], v[1]) + XMUL(m[6], v[2]) + m[7];
	v[2] = XMUL(m[8], v[0]) + XMUL(m[9], v[1]) + XMUL(m[10], v[2]) + m[11];
	v[0] = x;
	v[1] = y;
}

void vec4_xform(int32_t *v, const int32_t *m)
{
	int32_t x, y, z;
	x = XMUL(m[0], v[0]) + XMUL(m[1], v[1]) + XMUL(m[2], v[2]) + XMUL(m[3], v[3]);
	y = XMUL(m[4], v[0]) + XMUL(m[5], v[1]) + XMUL(m[6], v[2]) + XMUL(m[7], v[3]);
	z = XMUL(m[8], v[0]) + XMUL(m[9], v[1]) + XMUL(m[10], v[2]) + XMUL(m[11], v[3]);
	v[3] = XMUL(m[12], v[0]) + XMUL(m[13], v[1]) + XMUL(m[14], v[2]) + XMUL(m[15], v[3]);
	v[0] = x;
	v[1] = y;
	v[2] = z;
}

void mat_mult(int32_t *ma, const int32_t *mb)
{
	int i;
	int32_t res[16];
	int32_t *rptr = res;
	int32_t *row = ma;

	for(i=0; i<4; i++) {
		rptr[0] = XMUL(row[0], mb[0]) + XMUL(row[1], mb[4]) + XMUL(row[2], mb[8])  + XMUL(row[3], mb[12]);
		rptr[1] = XMUL(row[0], mb[1]) + XMUL(row[1], mb[5]) + XMUL(row[2], mb[9])  + XMUL(row[3], mb[13]);
		rptr[2] = XMUL(row[0], mb[2]) + XMUL(row[1], mb[6]) + XMUL(row[2], mb[10]) + XMUL(row[3], mb[14]);
		rptr[3] = XMUL(row[0], mb[3]) + XMUL(row[1], mb[7]) + XMUL(row[2], mb[11]) + XMUL(row[3], mb[15]);
		rptr += 4;
		row += 4;
	}

	memcpy(ma, res, sizeof res);
}

void mat_mult_pre(int32_t *ma, const int32_t *mb)
{
	int i;
	int32_t res[16];
	int32_t *rptr = res;
	int32_t *row = ma;

	for(i=0; i<4; i++) {
		rptr[0] = XMUL(row[0], mb[0]) + XMUL(row[4], mb[1]) + XMUL(row[8],  mb[2]) + XMUL(row[12], mb[3]);
		rptr[1] = XMUL(row[1], mb[0]) + XMUL(row[5], mb[1]) + XMUL(row[9],  mb[2]) + XMUL(row[13], mb[3]);
		rptr[2] = XMUL(row[2], mb[0]) + XMUL(row[6], mb[1]) + XMUL(row[10], mb[2]) + XMUL(row[14], mb[3]);
		rptr[3] = XMUL(row[3], mb[0]) + XMUL(row[7], mb[1]) + XMUL(row[11], mb[2]) + XMUL(row[15], mb[3]);
		rptr += 4;
		mb += 4;
	}

	memcpy(ma, res, sizeof res);
}


